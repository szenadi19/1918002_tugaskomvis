% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Img = handles.Img;
pixel_dist = str2double(get(handles.edit8,'String'));
GLCM = graycomatrix(Img,'Offset',[0 pixel_dist; -pixel_dist pixel_dist; -pixel_dist 0; -pixel_dist -pixel_dist]);
stats = graycoprops(GLCM,{'contrast','correlation','energy','homogeneity'});

Contrast = stats.Contrast;
Correlation = stats.Correlation;
Energy = stats.Energy;
Homogeneity = stats.Homogeneity;

jumlah = (mean(Homogeneity)+mean(Energy))/2;
set(handles.text13,'string',jumlah);
if (jumlah < 0.63)
    set (handles.text14, 'string', 'Kol Busuk');
end
if (jumlah >= 0.63)
    set (handles.text14, 'string', 'Kol Segar');
end;

set (handles.text9, 'string', num2str(Contrast(1)));
set (handles.text10, 'string', num2str(Correlation(1)));
set (handles.text11, 'string', num2str(Energy(1)));
set (handles.text12, 'string', num2str(Homogeneity(1)));

set (handles.edit3, 'string', num2str(mean(Contrast)));
set (handles.edit4, 'string', num2str(mean(Correlation)));
set (handles.edit5, 'string', num2str(mean(Energy)));
set (handles.edit6, 'string', num2str(mean(Homogeneity)));


